<h2>Jason<strong>Gu</strong>: Best Man</h2>

<h3>How We Met</h3><img class="placeholder" src="/images/placeholder.jpg"/>
<p>I honestly don't remember how we met. We knew each other from high school and badminton, so I'm sure it was one time we met while on the high school badminton team. Or maybe it was French class! For three years, we were in the same class starting with French 2A. Jason is definitely a personality - we hit it off quickly.</p>

<h3>Our Friendship</h3>
<p>It, like any other of my deep friendships, was one that just came naturally. We had a lot of similarities and the friendship just grew from there.  What I really remember about Jason was how adamant he was about his atheism in high school. It was ridiculous. Now, I wasn't a Christian then, but I wasn't really biased either way. Jason was really quick on his feet and he could rationalize anything. Give any argument about religion and he can knock it down. I didn't really enjoy what he was arguing about but I was amazed at his incredible intellectual prowess.<img class="placeholder1" src="/images/p.jpg"/> Imagine my surprise when, after he went to college, he became a Christian! How miraculous is God and praise Him for his work to save a fellow brother! Even then, I wasn't saved, but something about his transformation must've planted a seed in me.</p>

<p>Jason is the kind of friend who has always had an odd sense of humor. I don't remember any of the jokes, though, but even now there was one that was funny: "I wanted to collaborate with a girl in Berkeley and show her my samples. You know.. my field emissions gun." I think that joke somewhat sums up our friendship. We're a goofy pair, always trading jokes and jabs. Yet at the same time, we have our serious moments when we get a chance to talk. Jason put it best when he said, "it's one of those friendships where you just know that you can call anytime. You don't have to chat every single day, but when you need to, the other person is always there."</p>

<p>I love that about him and I really see him as the brother that I never really had in my life. I can't put it any more than that. Yeah, he's lazy sometimes and he doesn't want to work/drive/whatever sometimes, but when he really needs to, he'll always step up. The memories with him are simply endless. Our crazy moment: he, another friend Ben and I were racing to AMC Mercado to catch a movie. Literally racing. It was such a fun moment during our young and stupid lives. A dangerous one, for sure, but one where we felt like we were untouchable. Our serious moment: One night we wanted to talk. Jason had come back for break and we wanted to get a chance to talk. At 1am, we drove to Happy Donuts where we sat for a few hours just to catch up.</p>

<p>My friendship with Jason is simply indescribable. I've known him for over 10 years now and I never get tired of seeing him and talking to him. We're both passionate about the same things - from important things like God to trivial things like video games. We can talk about everything from bible verses and Christian perspectives to his thesis research. I mean, who else can talk to him about his frustrations with bombarding his sample with charged helium atoms to determine the bonding of hydrogen atoms and he doesn't have to explain what everything means?</p>

<p>And just recently I was surprised by his initiative in starting to organize the bachelor's party! I don't think I've ever seen that before. But really. I couldn't wish for a better friend than he and there is no hesitation on either side for having him as my best man.</p>

<h3>Where He Is Now</h3>
<p>Jason is currently at Carnegie Mellon University, where he is doing research for his doctorate in Materials Science. He sometimes serves as a Sunday School teacher for his church - Chinese Christian Alliance. </p>
