<h2>Flora <strong>Wong</strong>: Bridesmaid</h2>

<h3>How We Met</h3><img class="placeholder" src="/images/placeholder.jpg"/>
<p>Flora and I have always been cool since we first got to know each other through SCBC back in... oh 2003? (laugh). But "cool" is the only description for a long time because we never REALLY got to know each other until our "Girls' Group" started in January 2007. It all began when Jenny, Flora, and I were revealing to each other our "secrets" of having significant others in a party back in late 2006. It was HILARIOUS and a huge SHOCKER to all of us because we all thought that the others were single until then! Then, in yet another party Cora joined in for some heart-felt girl talks. After some prayers, I suggested for all four of us to get together regularly to dine and to share life together. We all excitedly agreed to it because, well... we LOVE to eat!! (and we like each other too.)  Since then, we have been meeting every month to try out new joints, share our trials, and celebrate the blessings that God pours on us.</p>

<h3>Our Friendship</h3>
<img class="placeholder1" src="/images/p.jpg"/><p>During this whole time, Flo and my friendship was still pretty "cool". Then one day, the two of us decided to take a hike while waiting for the other two girls for dinner. That was the first time I got to know her individually, and I don't think I have ever told her, but I became very impressed by her. I discovered that she is even more trustworthy that I already thought she was, and I especially admired her calmness and steadiness in dealing with relationships and life. Since then, I found myself gravitated toward her and her genuine ways. She is a great friend and is full of joy and laughs! I especially enjoyed our 3x3 prayer session together, where we got to pray for our family every day and chatted on the phone almost every night for 30 days. I appreciate her and I am proud of the strong, Godly woman that she is.</p>

<p>Oh and how could I forget to mention that she is the only American Born Chinese I know who could fire her Cantonese without a hint of American accent? I LOVE shooting back and forth Chinglish with my girlfriend!</p>
<br/>
<h3>Where She is Now</h3>
<p>Flo is a graphic designer living in Santa Clara. She is down for anything from worship to concert to outdoor activities to exploring new restaurants to having long, deep talks. (She's rolling her eyes now).</p>
