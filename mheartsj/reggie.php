<h2>Reggie<strong>Rogers</strong>: Groomsman</h2>

<h3>How We Met</h3><img class="placeholder" src="/images/placeholder.jpg"/>
<p>This is probably one of the funniest and oddest stories you'll ever hear in the history of meeting someone for the first time. I had just joined the Phi Beta Epsilon fraternity. Rush at universities like MIT is alcohol-free, so people can't wait to have some "fun" when the events are over. Now, I'm not one for doing much drinking. In fact, before I went to college, I rarely drank, other than for the once-in-a-while taste of wine or beer. However, this was college and a different time! (I definitely don't endorse this behavior.) So having joined the fraternity, when it was time to party, it was time to party! We broke out the hard liquor and competition began. I got to almost thirteen shots (in about thirty minutes) before I started noticing that the hallway was tipping a little bit. I then decided to go downstairs and see other people and make some friends. I was already having issues navigating the hallway, so the stairs were definitely an issue. Doing pretty good on the last few flights, I tried to get down the last set to the second floor, where the living room and pool room were. I took one step and tumbled down twenty steps - straight onto the floor at Reggie's feet.<img class="placeholder1" src="/images/p.jpg"/> He had this shocked look on his face and I said, "I'm alright! Just wanted to rest a bit."</p>

<h3>Our Friendship</h3>
<p>I don't think our friendship is what you would call a typical friendship in the sense that we're not exactly peers. After our aforementioned encounter, Reggie and I were family - he became my big brother. For that, I always look up to him as a source of reflection. I remember the time in college when we were roommates and we decided to put a book of poems together (now what happened to that book?!). He was the opposite of me: the calm, logical counterpart to my emotional and sometimes brash character. What I find somewhat peculiar about Reggie is that for someone who is quite good at listening and giving advice, he is somewhat of a quiet person, internalizing his thoughts and feelings. I wonder, as I'm writing this, who he goes to? He always gives this impression that he is calm and collected. Nothing fazes him!</p>

<p>The best way to describe Reggie is the same way that Jess describes her friends: "knows how to just be chill." Our friendship is very laid back. We can have extravagant plans like trips to whitewater rafting and so on, or just taking it easy at home watching TV.</p>

<h3>Where He Is Now</h3>
<p>Reggie is currently doing research for his PhD in Chemical Engineering at the University of Michigan. He has high hopes of moving to California after he graduates in December 2009. Meanwhile, he serves at the Vineyard Church of Ann Arbor on the worship team.</p>
