<h2>Aya <strong>Westbrook</strong>: Bridesmaid</h2>

<h3>How We Met</h3><img class="placeholder" src="/images/placeholder.jpg"/>
<p>For anyone who knows me, one of my greatest passions of all time is badminton. So it was a surprise that one of the very first things I had to do when I moved to LA was finding the gym and the people to play with. The first time I stepped foot into the UCLA court, I was sniffing out the players there and Aya caught my eyes right away.  She was playing mixed at the time and I remember thinking "dude she's pretty good..." So I patiently waited to play against her and her partner (Jason, who turned out to be a good friend as well as my partner-in-crime in forming the UCLA Badminton Team), had a ball of a time, and discovered the same night during dinner how much we love eating as well as playing badminton!</p>

<h3>Our Friendship</h3>
<p>Though Aya and I met during our first year in college, our friendship didn't truly blossom until our final years in school. I still remember that one summer night during our sophomore year after badminton club hours; we were sitting on my bed chatting when Aya suggested that we should room together. Unfortunately, I already had plans for the next year, so we made promises that we would be each other's last year roomies. It was definitely one of those "moment" things, since Aya and I were not extremely close then. Eventually, however, we became each other official badminton doubles partners and I was completely amazed by how fun and generous she was. Badminton practices would not have been the same without her positive attitude and energy. I will never forget the victory dances that we did after our good shots (song name not to be revealed, haha)! Third year was crazy for us in different ways. As I was going through some serious personal struggles, Aya was out of the country in Japan studying abroad (btw she is half-Japanese, half-Caucasian, fluent in written and spoken Japanese!) for what seemed like forever.  We missed each other a lot that year, but it was made up for during the last year when we roomed together. It was then that I truly got to her and the many wonderful ways about her. Mark my words: I am highly convicted that I will never find another woman as considerate, loving, strong, and understanding as she is. I have shared life with her and witnessed her incredible strength displayed in one of the most difficult times in her life.  It is easy to treat someone well when one is well, but for her to treat me with such love in her struggles was the greatest testament of her wonderful personality. We used to joke that if we were of the opposite sex we would have made a great couple. During the entire year that we lived together, we had never bickered, and always worked together to support one another. I love this girl to death - sometimes I feel so blessed of having her in my life that I have to pinch myself. :)</p>
