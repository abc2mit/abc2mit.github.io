<h2>Trista<strong>Chan</strong>: Bridesmaid</h2>
Though Trista was the last addition to the bridal party, she is by no means not special! As a matter of fact, we have been friends for the past 20+ years — that makes the longest friendship I have to date! Since it is now official that she can make it to Hawaii all the way from Hong Kong, there is no reason to not make her the honored, well-deserved bridesmaid that she is. :) 

<h3>How We Met</h3><img class="placeholder" src="/images/placeholder.jpg"/>
<p>Let's hop into a time machine and travel back to when we were barely 5 years old. The location was Holy Angels Canossian School in Hong Kong.  It was a nice sunny morning, the first day of our elementary school journey. My grandmother had been gone the last 15 minutes or so to do my registration, so I was left alone to play by myself.  I felt lonely and sad as I observed all the current students having fun with their groups of friends. Bored, I started leaping through and up and down the metal fence bar in front of me, imagining myself to be a gymnast. I was lost in my own fantasy land until I noticed her—another wide-eyed new girl waiting for her mother to finish up her <img class="placeholder4" src="/images/p.jpg"/>registration.</p>
<p>I was determined to make the first move.</p>
<p>"Um... would you like to be my friend?" (in Cantonese, of course)</p>
<p>Yes. I have never been shy. Not even as a kid. But I am especially glad that I was not shy that day, because that began one of the most awesome friendships I will ever have.</p>


<h3>Our Friendship</h3>
<p>Even though Trista and I were not always in the same class, she continued to be one of the closest friends I had, an exception during that time. (FYI in the HK school systems, you generally are the closest with the students in your own class, and our school had three classes for each grade). I loved her because she always had a great head on her shoulders, a lovely sense of humor, and most importantly, a non-judgmental spirit. I felt completely comfortable being myself around her.  We would do silly things like calling each other during certain shows and play our own voice-overs for the characters. We shared many silly secrets that all little girls had.</p>
<p>But life was not always easy... as others were preparing to advance to form one (synonymous to middle school here), I was preparing to leave Hong Kong to move to the United States. I was sad, of course, but I was hopeful that I would keep in touch with most everyone I cared about. Well, about half a year of massive letters-writing later, reality kicked in. I couldn't keep in touch with 20 people. After about a year, only a handful remained. Then, after many years, only Trista stood the test of time.</p>
<p>Looking back, I really don't know how we remained in touch, let alone how we became even better friends. Some calls it fate, some calls it luck, I believe that this is a friendship blessed by God. Every time Trista and I see each other (which is about every 2-3 years), it is as if we have never had the years apart growing up in two different cultures with the vast ocean between us. I am just as comfortable being completely myself around her as I was when I was 5 years old. When we went traveling to Hainan Island together three years ago, the people on the tour asked how we could spend three straight days talking to each other non-stop. Haha, only if they knew how long we can REALLY talk to each other for. ;)</p>
<p>I am overjoyed that she is able to attend my wedding all the way from Hong Kong. I look forward to having her standing next to me once again, this time as my bridesmaid, 20 years after her standing next to me as another wide-eyed new kid in school.</p>

<h3>Where She is Now</h3>
<p>Trista recently graduated from the prestigious Hong Kong University as a clinical psychologist.  She still resides in Hong Kong and is enjoying her new job in her field.</p>
