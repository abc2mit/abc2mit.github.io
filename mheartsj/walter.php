<h2>Walter<strong>Lin</strong>: Groomsman</h2>

<h3>How We Met</h3><img class="placeholder" src="/images/placeholder.jpg"/>
<p>I believe the first time we met was Campus Pre-frosh Weekend (CPW). Walter was a friend of someone who knew both Victor and myself. She had brought him to our fraternity, Phi Beta Epsilon, for a party and introduced him to me. I pretty much spent my time trying to convince him that he should come here to MIT instead of any other school. We were smart. We were fun. And I definitely didn't tell him how much work we had to put in. I think I had pretty much sold him and the friendship just grew from there. Granted, I wasn't the girl of his dreams by any means, but I think I came in second-best.</p>

<h3>Our Friendship</h3>
<p>It has been a brotherly friendship since the moment we met. I think I took him in as a mentor and an older brother. Not that I needed to (he has both and older brother and an older sister), but maybe I was more "fun" and more his age than they were. We were always there for each other. I think I gave him more advice than he did me, but it was always comforting to know that I had someone to hang out and just kick it.<img class="placeholder2" src="/images/p.jpg"/> We only took one class together: 5.12 (Organic Chemistry). Or was it two (7.05)? I don't remember anymore, but we had a great time and it was a rare occasion - considering I was two years older than him, it's a rare possibility that we get to take classes together. In fact, since he was studying Cognitive Sciences and was a pre-med, I think he was the one who really got me into doing a Biomedical Engineering minor. I don't think it was anything that he said (no, "Mike, I think you should do a minor in something other than finance."), but getting me to try something outside the realm of computer science and really showing me that it was <i>interesting</i>. He encouraged me to think outside the box.</p>

<p>Loyalty. That word alone sums up our friendship. I remember their being so much crap that was going on in the fraternity - so much to the point where I left the house. People, of course, took sides, as they always do. What I admired the most about Walter was that he had no hesitation in his mind that I had a good heart and good intentions. I don't know whether I had a great impression on him or I was just that good of a friend to him (besides the incident where I had to take care of him all night, I don't know if I was). But he stood by me, supporting me when I needed it most.</p>

<p>To this day, we still talk like brothers, sharing about what's been going on in our lives. It's that sort of relationship that is not awkward when we don't have time to catch up. When we do, there's always so much to talk about that it's refreshing.</p>

<h3>Where He Is Now</h3>
<p>Walter is currently studying for his Medical Degree at the Harvard University Medical School. Yes, he'll be a <i>real</i> doctor, unlike those wannabe PhD students! (I kid.)  Now that he's passed his boards with flying colors, he gets to go wherever he wants for his residency. That means, California! But not until 2011 :(</p>
