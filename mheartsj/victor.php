<h2>Victor<strong>Hsu</strong>: Groomsman</h2>

<h3>How We Met</h3><img class="placeholder" src="/images/placeholder.jpg"/>
<p>The way Victor and I met is pretty much coincidental (in that it was neither of our choice to) and very much similar to how Tiff and Jess met: as college roommates. The way it worked at MIT was that rush happened before the school year started. That meant that, during orientation, freshmen needed to have a temporary room to stay in until rush was over. At the end of rush, an incoming student was then either in a dorm or in a fraternity. Victor and I were assigned as roommates. I remember getting to the room (which, unfortunately, was the one furthest away from the main campus!) and seeing a lot of bags but no one there. I was bummed that my roommates had gotten there before me; besides being stuck with the top bunk, I really didn't know anyone or what I was supposed to do. After wandering around for a day, I finally met Victor in the room and we hit it off.</p>

<h3>Our Friendship</h3>
<p>From then on, it was mostly doing things together.<img class="placeholder1" src="/images/p.jpg"/> We practically rushed together and went to all of the free events together. I think Reggie, as the rush chairman for PBE, knew that and tried to keep us together at the house. Looking back at everything that happened, I don't think either of us would have become a fraternity brother if the other didn't join the same house. Victor and I ended up as number four and three, respectively. And even through that entire process of rushing and the like, we really bonded. He's a bit of the quieter, mellower type than I am. We trusted and respected each other; that really helped us through a lot of the pledging and initiation ordeals that we had to go through. I say ordeals as a politically correct term because you know how fraternities can be. Thankfully, what doesn't kill us only makes us stronger, right?</p>

<p>He was always the smarter one (or at least the most diligent one) and having him in the same class (both in terms of year in college and actual classes) was a saving grace. I can't even begin to count the number of times he let me copy his problem sets. If only I was good enough to memorize and understand the solutions, I might've done better in school, but it's the experience that counts, right? None of that other bureaucratic and academic stuff really matters after coming to the real world. And what a difference it was! School was where we were untouchable - that life was really a dream and different than high school. Sure, it was as hard as hell, but we were having fun and living the life. Neither of us were really the party type, so it wasn't like we were going crazy or anything, but we really enjoyed the experiences we shared: working late night on projects or problem sets, making runs to the student center market in the bitter cold because we were hungry, calling Domino's in the middle of the night for dinner because we came back too late to eat dinner at the house. The list goes on.</p>

<h3>Where He Is Now</h3>
<p>Victor is currently residing in Chicago, IL, studying for his MBA at Northwestern's Kellogg School of Management. He loves to travel and still plays the violin enthusiastically (without the prodding of his parents!) in some concerts. I'm still trying to convince him to move to California after he graduates.</p>
