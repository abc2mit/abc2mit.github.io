<h3>How We Met</h3>
<p>Well well well... Let me rewind to 13 years ago, when I first noticed a very quiet Filipino boy with his purple jacket, semi-shinny lip balm, and neatly gelled-back hair. Every single day, while everyone was waiting for our Ms. Weiss' class, he would sit in the same spot on the bench with his upper back folding slightly forward, his elbows on his laps, his right hand curled on top of his left, and his lower face over his hands. That was the way he sat before her class, every single day. Of course I found him a bit strange! "Does he even talk?" I thought. I didn't think so, until one day, out of the blue, he asked me if I had email. I thought he was crazy, because I thought he was saying "Amiel" (his name) rather than "email".  I probably misheard because I was so shocked that he spoke, especially to me. By the way, you have to remember that 13 years ago, very few kids knew about emails, let along actively used them.  Well, turns out we were two of the few :D. And as it turned out... we both already had a website of our own (nerds, unite!).<img class="placeholder1" src="/images/p.jpg"/> So we, along with a couple other email users whom he also discovered, would share knowledge on building websites and chatted in chat rooms (I am a little embarrassed as I am writing this). It was hilarious because I remembered one of the guys would always boast about the number of emails he received the day before... hahaha! (I wasn't THAT nerdy.)</p>

<h3>Our Friendship</h3>
<p>Our first interaction was atypical, yes, and our friendship was even more so. It was never the kind of friendship that required a lot of talking; the top priority was not a complete understanding of each other, but a genuine heart to support and uplift each other through thick and thin. In the past so many years, we were there for the other in every milestone of the journey of life. We struggled together during the first years when we moved from our birth countries to the US. He was the brave soul who was willing to ride in my car after I first got my license. He stood next to me in our middle school and high school graduations, and celebrated with me in my college one. We have had our laughs over each other's stupid mistakes.  We have had our fights too (some of them bad ones), but they were always of things you only bring up because you cared enough to, the kinds of things that no one else would dare to point out.  Amiel would tell you of the years that he has known me, Mike is the only one that he would entrust in giving me happiness for the rest of my life (and boy do I agree!). Amiel is the most loyal friend I have ever had, a brother for life.</p>

<h3>Where He Is Now</h3>
<p>Amiel is currently residing in LA and pursuing his passion in the Arts Institute in Santa Monica.</p>
