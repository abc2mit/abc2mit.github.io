<h2>Jennifer<strong>Wu</strong>: Bridesmaid</h2>

<h3>How We Met</h3><img class="placeholder" src="/images/placeholder.jpg"/>
<p>When I first moved from UCLA back to the Bay Area, I was quite sad because the majority of my friends were in LA. Though I have had already gone to South Bay Chinese Baptist Church (SCBC) for a couple of years prior to then, I was only attending whenever I was home from college.  Then another half a year went by, and I started feeling very comfortable with almost everyone at church except... well, Jen!  I have always seen her around, and I have known her as my friend's sister. I would see her in church, Sunday school, joint fellowships, girls' dinners, group outings, but we would just never talk to each other! I remember trying to get to know her and even drew her a fancy card for her birthday... but She was a big mystery to me because she was so quiet! I prayed about it but almost gave up all together.</p>

<p>But God has a sense of humor and He has His perfect ways in bringing people together. It was January 2006, when Jen's parents were going to board their cruise in San Francisco.<img class="placeholder1" src="/images/p.jpg"/> Jen knew that I was on vacation as well, so she asked me out of the blue to go with her to the City to drop her parents off. Of course I jumped on the opportunity! I seriously felt like I was going on a date that day... we went and explored around San Francisco in the day time and then Berkeley at night. We chatted about anything and everything, our past and present. We documented our whole day with heart-felt photos (Jen took one of her favorite pictures of me that day, the "posed unposed" laugh at the top of the Coit Tower).  Then it started a streak of hang out chain, a continuous 13 days to be exact (if I am remembering correctly). Even after that, we would see each other ALL THE TIME. It was really refreshing to get to know someone whom I thought I probably would never talk to, let alone get along with.</p>

<h3>Our Friendship</h3>
<p>No pain, no gain. Our friendship runs deep, but it did not always come easy or natural. We can share almost everything with one another, and we know we would be there for each other no matter what, but we are very different in the way we live and think. In these past almost 3 years that we have come to know each other, we have gone through a lot of individual growing pain together. I recall countless memories of tears and laughter shared together. There were also moments of frustration, but it was only as a result of love and care for the other. In the end, we fall and we learn together, and we both know we have each a sister who would not give up. One of my favorite memory of our friendship was when I was reading through her Xanga entry which summed up her reflection for the year. It was exclusively regarding her family, and she went through and gave thanks for each of her family members. I was filled with tears when she spoke of me at the end of her list, referring me to a part of her family, as one of her sisters. For a person who has does not have a biological sister, I was overwhelmed with joy!</p>

<h3>Where She is Now</h3>
<p>Jen is currently residing in San Jose, working for the City of San Jose Correctional Facility, and enjoying her boyfriend's softball playoffs on her spare time =).</p>
