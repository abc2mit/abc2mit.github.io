<h2>Tiffany<strong>Chen</strong>Zhu: Matron of Honor</h2>

<h3>How We Met</h3><img class="placeholder" src="/images/placeholder.jpg"/>
<p>Like the majority of freshmen in UCLA, I checked the box for the school to randomly select my roommate when I applied for my dorm... and like most people, I was a little nervous of who I was going to get! I still remember receiving the first email from Tiff introducing herself to me the summer before, telling me that "she was an ethnomusicology major from the OC, likes green, doesn't put much makeup, but likes to party." That just made me even more nervous because I was a computer science engineering major from the Bay Area, likes pink, a self-declared makeup guru, and didn't party. Then, things got even more interesting when we first met. By the time my family and I drove me down to LA, Tiff had already fully settled in our room and had gone out. I was frowning upon the nastiness my unoccupied side of the room and decided to equip myself for a full cleaning process. So... half an hour into my hardcore cleaning, Tiff and her friend strolled in while I was spraying Lysol with my apron and latex gloves on!!!!!! Tiff's reaction was priceless... and while she was trying to rid of her shock, I was checking her out in her short shorts and finely groomed eyebrows.<img class="placeholder1" src="/images/p.jpg"/> It was one of the most awkward yet hilarious moments of my life.</p>

<h3>Our Friendship, Part I: Fun Times</h3>
<p>Let me tell you, I became fully convicted of the old saying, "don't judge the book by its cover" as the friendship between Tiff and I grew and blossomed. If we each would have held on to our first impression of each other, we would have had a nightmare of a time during our first year in college. Well, as it turned out, she was actually kind of cool. (I am cracking up as I am typing this.) No... I take that back. She is one of the most awesome, funny, caring, and down-to-earth girl I have ever known. It really didn't take that long at all before we realized how insanely similar we actually are (note how I complimented her before I made this statement :P). By the end of the first day we met, we were literally cracking up together on the floor while playing pool with a group of people. From then on, it was pure magic. We soon found out that we shared some very similar if not the same clothes, taste in music, food, perspective in life, and personality. It was not long until we were pretty well known on our floor and among our friends to be the infamously loud and crazy roommates. I wish I could incite all the ridiculously funny memories that we shared when we roomed together, but there were just too many!! I would have to say that one of my most missed thing in life is to stay up with her randomly till the wee hours in the morning blasting music and singing/dancing like it is going out of fashion. Of course, there were many incriminating photos of us dressing up,<img class="placeholder3" src="/images/p.jpg"/> making gangsta and ugly faces during these late-night sessions... and if you are lucky, you have probably seen (if not own) some of these classic pictures.</p>

<h3>Our Friendship, Part II: God's Sovereignty</h3>
<p>If we would have just continued our friendship in that direction, we would have been good friends, but never the best friends and sisters that we are now. Up until our late freshman year, we experienced a lot of bliss from shear ignorance and craziness, but things started to become more serious for the both of us in terms of our relationships and families. Even though we had similar personalities, I started to party a lot more while Tiff began to develop her relationship with the Lord. As my struggles became greater in the next two years, Tiff's faith grew stronger. When I thought my life was at its rock bottom, Tiff was one of the few people whom I could truly entrust in. Though I fought the idea of Christianity many times, Tiff was always faithful in praying for me and my salvation. In return, God was faithful in answering her prayers. My life would have never been the same if it was not because God had used her as a tool to bring me to Him. During that time of hardships and tears, our friendship had grown from its superficiality into a true sisterhood, in Christ. With the years passing by, as each of our walk became stronger in the Lord, so has our relationship with each other. She is one of the biggest blessings in my life and I look forward to many more years to sharing life together with her.</p>

<h3>Where She is Now</h3>
<p>Tiff is recently married to Randy Zhu, and the love birds are currently residing in Southern California. They are faithfully serving together in their church in Anaheim, lifting up the youths in their ministry. Tiff is also a dedicated math teacher to her middle school students.</p>
